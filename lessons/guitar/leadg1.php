        <p>In this lead guitar lesson, we'll be continuing to learn about how to use the natural minor scale on the guitar. As a guitarist, it's easy to fall into the habit of just playing through scales, and not actually making any music out of what you're playing. A good place to start with making your playing sound more melodic is to focus on playing chord tones. In this lesson, we'll be using a C minor scale, C minor pentatonic scale, and C minor arpeggio to learn about how to do this properly.</p> <br>
        <h2>The C Minor Scale</h2>
        <p>We'll start with the C minor scale since it's the foundation for everything we're doing in this lesson. Before you go any further, it's important to know all the note names in the C minor scale. The notes in a C minor scale are C D Eb F G Ab Bb.</p> <br>
        <img src="images/c-minor-scale-2.png" alt="No image found"> <br>
        <p>Get familiar with the standard C minor scale shape. While you're playing through this scale, pay close attention to the root notes (C notes) and work on emphasizing them. Those are your most important notes in this scale.</p> <br>
        <h2>C Minor Arpeggio</h2>
        <p>A great way to start learning about chord tones is to overlay a basic arpeggio over our scale shape. In this case, we'll be laying a C minor arpeggio over our C minor scale.</p> <br>
        <img src="images/c-minor-arpeggio.png" alt="No image found">
        <p>You'll notice there are three scale degrees used in this shape. 1, b3, and 5. These refer to the three notes found in a minor chord. These will be your target notes when playing over a C minor chord like the one in the downloadable jam track.</p> <br>
        <h2>Chord Tone Exercise</h2>
        <p>A great exercise for tying this all together is to start by ascending through the C minor arpeggio, then descending through the C minor arpeggio. Next, ascend through the C minor scale, and come back down the C minor scale. The last step is to go up and down the C minor pentatonic scale.</p> <br>
        <img src="images/c-minor-pentatonic.png" alt="No image found">
        <p>What this does is get you visualizing the chord tones within both the C minor scale and the C minor pentatonic scale. This will help you to be able to target specific notes within each scale that will sound best.</p> <br>
        <p>If someone told you to place your finger on the first fret, you would go to the first fret and place your finger right behind the fret. If they told you to go to the fifth fret on the first string with your first finger, you would count up five frets and place your first finger behind that fret on the thinnest string.</p> <br>
