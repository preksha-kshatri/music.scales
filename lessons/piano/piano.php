        <h2>Chapter 3: Basic Skills</h2>
        <h3>3.1 Keyboard Navigation and Musical Alphabet</h3>
        <p>The piano keyboard may look dizzying with its vast array of keys (88, to be exact). But once you understand the patterns that make it work, it's actually quite simple.</p> <br>
        <h4>How to Find Middle C</h4>
        <p>Take a look at your keyboard. Notice the black keys‚Äîsee how there are sets of two and sets of three? We'll use these sets of twos and threes to find Middle C.</p> <br>
        <video muted autoplay loop>
            <source src="images/C.mp4">
        </video> <br>
        <p>Middle C is the first note you'll learn on the piano. It's smack dab in the middle of the keyboard, usually where the brand name of the piano is.</p> <br>
        <p>Find a set of two black keys in the middle and play the white key on the very left: this is Middle C!</p> <br>
        <p>Remember where Middle C is. You'll use it to orient yourself around the rest of the keyboard.</p> <br>
        <h4>The Musical Alphabet</h4>
        <p>Now for your first theory lesson! In music, we name notes after the alphabet. The white key after C is D, the one after is E, and so on. The musical alphabet stops at G, however, so instead of a note called H we just repeat from C again.</p> <br>
        <img src="images/labelled keyboard.png" alt="No image">
        <p>If you're wondering what all those black keys are called, they're half-steps between the white notes and they're called sharps or flats. We'll cover sharps and flats more in the theory section of this guide.</p> <br>
        <p>Once you understand the musical alphabet, you can move on to the five-finger scale!</p>

        <h3 id="ls4">3.2 How to Play the Five Finger Scale</h3>
        <p>The five-finger scale is a basic skill that will introduce you to a finger movement that is fundamental to piano-playing.</p> <br>
        <h4>The Right Hand</h4>
        <video muted autoplay loop>
            <source src="images/R.mp4">
        </video> <br>
        <p>Let's go back to Middle C. Place your right thumb on Middle C. In piano, we call this finger 1.</p> <br>
        <p>Next, place your index finger on the next white key, D. This is finger 2. Place your remaining fingers on the other white keys. This means finger 3 goes on E, finger 4 goes on F, and finger 5 (pinky) goes on G.</p><br>
        <p>Try playing these notes in order (1-2-3-4-5). Then, play it in reverse (5-4-3-2-1). Congratulations, you've just played your first C major five-finger scale!</p> <br>
        <p>If this feels weird at first, that's totally normal and okay. It's a new movement, so it'll take time to get used to. Your fingers may want to stick together, especially fingers 3 and 4. To improve, challenge yourself to play all five notes as evenly and articulated as you can.</p> <br>
        <p>Play the right-hand five-finger scale a few times. Once you get the hang of it, move on to the left hand.</p> <br>
        <h4>The Left Hand</h4>
        <video muted autoplay loop>
            <source src="images/L.mp4">
        </video> <br>
        <p>Find the C below Middle C and place your left pinky (finger 5) on it. Then follow up with the rest of your left-hand fingers.</p> <br>
        <p>Again, play these notes up and down a few times until it feels more familiar.</p><br>

        <h3 id="ls5">3.3 Your First Chords</h3>
        <p>Chords are the building blocks of music. They're used in most Western music traditions, including pop, jazz, and church songs. Understanding chords will help you progress in piano on both the practical and theoretical fronts.</p> <br>
        <img class="ig" src="images/chord-positions-2x2 (1).png" alt="No image">
        <p>Now, there's a lot of theory behind chords and chord progressions ‚Äî theory that's quite fascinating ‚Äî but to make things simple in this guide, we'll learn four basic chords in their root position and how to move between them. </p> <br>
        <p>The names of the four basic chords we'll learn are: C major, G major, A minor, and F major.</p> <br>
        <img class="ig" src="images/chord-positions-rt (1).png" alt="No image">
        <p>On your right hand, place finger 1 (thumb) on C and finger 5 (pinky) on G. Play these notes together. They are the outer ‚Äúshell‚Äù of the C major root position chord. Once this feels comfortable, try playing E with finger 3 (your middle finger).</p> <br>
        <img class="ig" src="images/chord-positions-lh (1).png" alt="No image">
        <p>Try the same thing on your left hand. Place your left-hand finger 5 on C and finger 1 on G to form the chord shell. Then, play E with finger 3.</p> <br>

        <h3 id="ls6">3.4 Playing with Both Hands</h3>
        <p>Let's get one thing out of the way: playing with both hands feels weird. At least at first.</p> <br>
        <p>Hand independence is something you'll continually work on as a pianist. Even advanced pianists have challenges with hand independence.</p> <br>
        <img src="images/blurry hands.jpeg" alt="No image">
        <h4>The Five-Finger Scale in Both Hands</h4>
        <p>You already know the five-finger scale. Now let's try playing it with both hands.</p> <br>
        <p>When you play the five-finger scale up with both hands, the fingering with look like this:</p> <br>
        <pre>
        Right hand: 1-2-3-4-5 (C-D-E-F-G)
        Left hand: 5-4-3-2-1 (C-D-E-F-G)
        </pre> <br>
        <p>When you play the five-finger scale down with both hands, the fingering will look like this:</p> <br>
        <pre>
        Right hand: 5-4-3-2-1 (G-F-E-D-C)
        Left hand: 1-2-3-4-5 (G-F-E-D-C)
        </pre>
        <p>If this feels easy, try playing loudly in one hand and softly in the other. Then, switch.</p> <br>

