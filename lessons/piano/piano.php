        <h2>Chapter 4: The Grand Staff</h2>
        <h3>4.1 How to Read Music Notation</h3>
        <p>You know the musical alphabet. Now let's learn how to spell some music!</p> <br>
        <p>The first thing you should familiarize yourself with is the grand staff.</p>
        <img class="ig" src="images/grand staff labelled.png" alt="No image">
        <p>Most piano music is written on the grand staff. The grand staff is special to the piano because there's a top and bottom part, usually denoting right and left hands.</p> <br>
        <p>We'll briefly touch on the parts of the grand staff here, but for a more thorough breakdown, we recommend watching this lesson.</p> <br>
        <img class="gb" src="images/treble clef copy.png" alt="No image">
        <p>In general, pianists play notes on the top staff, the one with the treble clef, with the right hand.</p> <br>
        <img class="ga" src="images/bass clef copy.png" alt="No image">
        <p>Notes on the bottom staff, the one with the bass clef, are usually played with the left hand.</p> <br>
        <p>In each staff (treble and bass), there are five lines with spaces between them. This is where our notes sit.</p> <br>
        <img src="images/las.jpg" alt="No image">
        <p>The higher up we move the lines and spaces, the higher the pitch!</p> <br>
        <p>Notes occupy lines and spaces and they all have names. At first, it'll feel intimidating to memorize all the notes and their locations, so these acronyms might help.</p> <br>

        <h3 id="ls9">4.2 Music Symbols</h3>
        <p>You can probably tell that there's a lot more than notes and staves in sheet music. Here are other things you may run into when sight-reading music.</p> <br>
        <h4>Accidentals</h4>
        <p>A sharp means we raise a note up by one half-step. A flat means we lower a note down by one half-step. Therefore, F sharp is the black key immediately right of F, and B flat is the black key immediately left of B.</p> <br>
        <p>A natural tells you not to sharp or flat a note.</p> <br>
        <img src="images/sonata in c - accidentals.png" alt="No image">
        <p>Accidentals are sharps, flats, and naturals outside the key signature that appear in music. The general rule is, if a note has a sharp (or flat, or natural), all instances of that note thereafter in the same measure should also be sharped.</p> <br>
        <h4>Dynamics</h4>
        <p>Dynamics are what makes a good piano performance sound great.</p> <br>
        <p>Dynamics are markings that tell players whether to play something loud, soft, or something in between. Here are some common dynamic markings:</p>
        <ul>
            <li>Forte (f) - loud</li>
            <li>Piano (p) - soft</li>
            <li>Mezzo forte (mf) - medium loud</li>
            <li>Mezzo piano (mp) - medium soft</li>
            <li>Fortissimo (ff) - very loud</li>
            <li>Pianissimo (pp) - very soft</li>
            <li>Crescendo - gradually get louder</li>
            <li>Decrescendo - gradually get softer</li>
        </ul>
